# Designing and Debugging
# Every if statement must have an else
# If this else should never run because it doesn't make since: use the die function
# Not really a function anymore (die)
# Treat if-statements like paragraphs, if-elif-else grouping is like a set of setnences
# blank lines before and after
# Boolean tests should be simple, if complex, move their calculations to variables earlier
# in your function and use a good name for the variables

# Rules for Loops
# While loop only to loop forever, probably never
# Use a for-loop if there is a fixed or limited number of things to loop over

# Tips for Debugging
# Don't use a debugger, it's confusing
# Use print statements to print out the values of variables at points in the program to 
# see where they are going wrong
# Make sure parts of your programs work as you work on them. Don't write massive files of
# code before you try to run them. Code, run, fix (a little).

